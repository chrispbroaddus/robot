int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration----------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* Configure the system clock */
  SystemClock_Config();

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_TIM1_Init();
  MX_UART5_Init();
  MX_USB_DEVICE_Init();

  /* USER CODE BEGIN 2 */
  HAL_GPIO_WritePin(LD4_GPIO_Port, LD4_Pin, GPIO_PIN_SET); // Green on -> init complete
  OnStartup();
  HAL_Delay(1000);

  // Motor configuration based on whether we're LCU or RCU
  static struct CommandHandlerConfiguration rcu_config = {{{1, +0.4, -0.33}, {0, +0.4, 0.0}, {0, +0.4, +0.33}}};
  static struct CommandHandlerConfiguration lcu_config = {{{1, -0.4, -0.33}, {1, -0.4, 0.0}, {0, -0.4, +0.33}}};
  int is_rcu = HAL_GPIO_ReadPin(IS_RCU_b_GPIO_Port, IS_RCU_b_Pin) == GPIO_PIN_RESET;
  int is_lcu = HAL_GPIO_ReadPin(IS_LCU_b_GPIO_Port, IS_LCU_b_Pin) == GPIO_PIN_RESET;
  if (is_rcu == is_lcu) {
	  HAL_GPIO_WritePin(LD4_GPIO_Port, LD4_Pin, GPIO_PIN_RESET); // Green off -> VCU id failed
	  for(;;);
  }
  OnInitialization(is_rcu ? &rcu_config : &lcu_config);
  uint16_t flash_code = is_rcu ? 0x00FF : 0x033F;

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
  /* USER CODE END WHILE */

  /* USER CODE BEGIN 3 */

	  // Execute this loop at ~30 Hz
	  uint32_t delay_ms = 33;
	  uint32_t tickstart = HAL_GetTick();

	  // Indicate if USB up -- orange == usb connected, and not transmitting
	  extern volatile int cdc_usb_initialized;
	  HAL_GPIO_WritePin(LD3_GPIO_Port, LD3_Pin, cdc_usb_initialized ? GPIO_PIN_SET : GPIO_PIN_RESET);

	  // Process pending command, if any
	  OnMainLoop();

	  // Flash the green LED at ~3 Hz; green LED flashing == main is running
	  HAL_GPIO_WritePin(LD4_GPIO_Port, LD4_Pin, flash_code & 1 ? GPIO_PIN_SET : GPIO_PIN_RESET);
	  flash_code = (flash_code << 1) | (flash_code >> (8 * sizeof(flash_code) - 1));

	  // Execute this loop at ~30 Hz
	  while (HAL_GetTick() - tickstart < delay_ms) {}
  }
  /* USER CODE END 3 */

}

/** System Clock Configuration
*/
void SystemClock_Config(void)
{

  RCC_OscInitTypeDef RCC_OscInitStruct;
  RCC_ClkInitTypeDef RCC_ClkInitStruct;

    /**Configure the main internal regulator output voltage 
    */
  __HAL_RCC_PWR_CLK_ENABLE();

  __HAL_PWR_VOLTAGESCALING_CONFIG(PWR_REGULATOR_VOLTAGE_SCALE1);

    /**Initializes the CPU, AHB and APB busses clocks 
    */
  RCC_OscInitStruct.OscillatorType = RCC_OSCILLATORTYPE_HSE;
  RCC_OscInitStruct.HSEState = RCC_HSE_ON;
  RCC_OscInitStruct.PLL.PLLState = RCC_PLL_ON;
  RCC_OscInitStruct.PLL.PLLSource = RCC_PLLSOURCE_HSE;
  RCC_OscInitStruct.PLL.PLLM = 8;
  RCC_OscInitStruct.PLL.PLLN = 336;
  RCC_OscInitStruct.PLL.PLLP = RCC_PLLP_DIV2;
  RCC_OscInitStruct.PLL.PLLQ = 7;
  if (HAL_RCC_OscConfig(&RCC_OscInitStruct) != HAL_OK)
  {
    Error_Handler();
  }

    /**Initializes the CPU, AHB and APB busses clocks 
    */
  RCC_ClkInitStruct.ClockType = RCC_CLOCKTYPE_HCLK|RCC_CLOCKTYPE_SYSCLK
                              |RCC_CLOCKTYPE_PCLK1|RCC_CLOCKTYPE_PCLK2;
  RCC_ClkInitStruct.SYSCLKSource = RCC_SYSCLKSOURCE_PLLCLK;
  RCC_ClkInitStruct.AHBCLKDivider = RCC_SYSCLK_DIV1;
  RCC_ClkInitStruct.APB1CLKDivider = RCC_HCLK_DIV4;
  RCC_ClkInitStruct.APB2CLKDivider = RCC_HCLK_DIV2;

  if (HAL_RCC_ClockConfig(&RCC_ClkInitStruct, FLASH_LATENCY_5) != HAL_OK)
  {
    Error_Handler();
  }

    /**Configure the Systick interrupt time 
    */
  HAL_SYSTICK_Config(HAL_RCC_GetHCLKFreq()/1000);

    /**Configure the Systick 
    */
  HAL_SYSTICK_CLKSourceConfig(SYSTICK_CLKSOURCE_HCLK);

  /* SysTick_IRQn interrupt configuration */
  HAL_NVIC_SetPriority(SysTick_IRQn, 0, 0);
}

